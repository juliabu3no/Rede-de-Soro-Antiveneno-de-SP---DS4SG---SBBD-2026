def separar_casos(df_animal):
    """
    Separa os casos por município de atendimento/notificação (Destino)
    e por município de ocorrência do acidente (Origem).
    """

    df_not_mun = (
        pd.crosstab(index=df_animal['Destino'], columns='Frequência')
        .reset_index()
        .rename(columns={'Destino': 'Codigo Municipio'})
    )

    df_aci_mun = (
        pd.crosstab(index=df_animal['Origem'], columns='Frequência')
        .reset_index()
        .rename(columns={'Origem': 'Codigo Municipio'})
    )

    return df_not_mun, df_aci_mun

def contar_casos(df_not_mun, df_aci_mun, gdf_munic):
    """
    Junta as frequências de casos ao GeoDataFrame de municípios.
    """

    mun_not = gdf_munic.merge(
        df_not_mun,
        on='Codigo Municipio',
        how='left'
    )

    mun_aci = gdf_munic.merge(
        df_aci_mun,
        on='Codigo Municipio',
        how='left'
    )

    mun_not['Frequência'] = mun_not['Frequência'].fillna(0).astype(int)
    mun_aci['Frequência'] = mun_aci['Frequência'].fillna(0).astype(int)

    mun_not['NUM_CASOS_LOG'] = np.log1p(mun_not['Frequência'])
    mun_aci['NUM_CASOS_LOG'] = np.log1p(mun_aci['Frequência'])

    return mun_not, mun_aci

