import numpy as np

def distancia_haversine_km(p1, p2):
    """
    Calcula distância em linha reta entre dois pontos geográficos em km.
    Espera pontos shapely Point, com:
    x = longitude
    y = latitude
    """

    if p1 is None or p2 is None:
        return np.nan

    if pd.isna(p1) or pd.isna(p2):
        return np.nan

    lat1, lon1 = p1.y, p1.x
    lat2, lon2 = p2.y, p2.x

    R = 6371  # raio médio da Terra em km

    lat1_rad = np.radians(lat1)
    lat2_rad = np.radians(lat2)
    dlat = np.radians(lat2 - lat1)
    dlon = np.radians(lon2 - lon1)

    a = (
        np.sin(dlat / 2) ** 2
        + np.cos(lat1_rad) * np.cos(lat2_rad) * np.sin(dlon / 2) ** 2
    )

    c = 2 * np.arcsin(np.sqrt(a))

    return R * c

