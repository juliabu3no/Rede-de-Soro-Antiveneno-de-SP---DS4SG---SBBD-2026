def separar_casos_anual(df_animal, menor_ano, maior_ano):
    """
    Separa os casos por município de atendimento/notificação (Destino)
    e por município de ocorrência do acidente (Origem)
    para cada ano.
    """

    # Filtra os dados
    df_animal = df_animal[
        (df_animal['Ano'] >= menor_ano) &
        (df_animal['Ano'] <= maior_ano)
    ]

    # Destino por Ano
    df_not_mun = df_animal.groupby(['Destino','Ano']).size().reset_index(name='Frequência')
    df_not_mun = df_not_mun.rename(columns={'Destino': 'Codigo Municipio'})

    # Origem por Ano
    df_aci_mun = df_animal.groupby(['Origem','Ano']).size().reset_index(name='Frequência')
    df_aci_mun = df_aci_mun.rename(columns={'Origem': 'Codigo Municipio'})

    return df_not_mun, df_aci_mun

