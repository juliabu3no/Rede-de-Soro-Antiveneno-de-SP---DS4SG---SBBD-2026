  casos_total = df_mun['Frequência'].sum()
  anos = len(df_mun['Ano'].unique())

  taxa_referencia = (casos_total / pop_total) / anos

  # Espectativa
  df_final['Casos Esperados Anual'] = df_final['Populacao Estimada IBGE 2022'] * taxa_referencia
  df_final['Casos Esperados Período'] = df_final['Casos Esperados Anual'] * anos

  # SMR = Casos Observados / Casos Esperados
  df_final['SMR'] = df_final['Frequência'] / df_final['Casos Esperados Período']

  #
  df_final['Taxa Padronizada'] = df_final['SMR'] * (taxa_referencia * 100000)

  return df_final

